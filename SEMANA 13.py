#Definimos la función promedio_temperaturas que recibirá como parametro las temperaturas a analizar
def promedio_temperaturas(temperaturas):
    #Definimos una variable auxiliar que nos permitirá almacenar los resultados de las operaciones
    promedios_temperaturas = []
    #Recorremos el array de temperaturas
    for ciudad in temperaturas:
        # Definimos una variable auxiliar que nos permitirá almacenar los resultados de las operaciones
        promedios_semana = []
        # Recorremos la segunda dimensión del array  (ciudad)
        for semana in ciudad:
            #Guardamos en una variable el tamaño del array
            num_dias = len(semana)
            #Sumamos cada una de las temperaturas iterando cada día de la semana
            suma_temperaturas_semana = sum(dia["temp"] for dia in semana)
            #Realizamos el promedio dividiendo el número de días para la suma total de temperaturas
            promedio_semana = suma_temperaturas_semana / num_dias
            #Guardamos el resultado en el array promedios semana
            promedios_semana.append(promedio_semana)
        #Calculamos el promedio total de la ciudad sumando los promedios semanales y dividirlos para el número de semanas
        promedio_total_ciudad = sum(promedios_semana) / len(promedios_semana)
        #Guardamos el resultado en el array promedios_temperaturas
        promedios_temperaturas.append(promedios_semana)
        #Mostramos el promedio general de la ciudad
        print("Promedio total de temperaturas para la Ciudad", len(promedios_temperaturas), ":", promedio_total_ciudad)
    for i, ciudad in enumerate(promedios_temperaturas):
        #Recorremos el array promedios_temperaturas para nombrar la ciudad
        print("Ciudad", i + 1)
        for j, semana in enumerate(ciudad):
            # Recorremos el array promedios_temperaturas para dar el resultado de la semana
            print("Semana", j + 1, ":", semana)

